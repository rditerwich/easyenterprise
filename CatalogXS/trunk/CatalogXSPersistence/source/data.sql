K+E Novaboard F990 Protect

HOOGGEPIGMENTEERDE OXIDATIEF DROGENDE VIERKLEURENSERIE

Met beperkte bakfrisse eigenschappen en zeer hoge schuurvastheid.
Inzetbaar op één- en meerkleuren offsetpersen. Ook zeer geschikt voor het schoon en weer
drukken op machines met omkeertrommel. De extreem goede schuurvastheidseigenschappen
maken de inkt zeer geschikt voor het bedrukken van moeilijke papiersoorten, zoals matgestreken
papier en karton. De C990 Protect heeft een snelle oxidatieve droging en een hoge glans. Door
het gebruik van speciale bindmiddelen heeft deze inkt in beperkte mate bakfrisse eigenschappen
wat de verwerking op de pers ten goede komt.
Kenmerken van de inkt:
De C990 Protect zal niet snel gaan stuiven en is heel stabiel. De gebruikte bindmiddelen zorgen
voor minimaal opbouwen op tegendrukcillinders.
Verdere kenmerken zijn: snelle wegslag en een snelle verderverwerking van het drukwerk is
goed mogelijk. De inkt heeft zeer goede vochtopnemende eigenschappen, ook op persen met een
minder goed afstelbaar vochtsysteem.
De C990 Protect is door de goede inkt/vochtbalans ook goed inzetbaar voor alcoholvrij drukken.

